
s = input("Введите строку: ")

p = 0
b = 0

for i in range(len(s)):
    if s[i] in ['+', '-']:
        p += 1
        if i < len(s) - 1 and s[i + 1] == '0':
            b += 1

print(f"Общее количество символов '+' и '-': {p}")
print(f"Количество символов '+' и '-' перед нулем: {b}")
